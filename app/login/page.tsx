'use client'

import { useState } from 'react'
import { supabase } from '@/lib/supabase/client'
import { motion } from 'framer-motion'
import { Sparkles, Loader2 } from 'lucide-react'

export default function LoginPage() {
  const [isLoading, setIsLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)

  const handleGoogleLogin = async () => {
    setIsLoading(true)
    setError(null)

    try {
      // 환경에 따른 redirect URL 설정
      const getRedirectUrl = () => {
        const currentOrigin = window.location.origin

        // 로컬 개발 환경에서는 localhost 유지
        if (currentOrigin.includes('localhost')) {
          return `${currentOrigin}/auth/callback`
        }

        // 프로덕션 환경에서는 현재 도메인 사용
        return `${currentOrigin}/auth/callback`
      }

      const { error: authError } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: {
          redirectTo: getRedirectUrl(),
          // 매번 계정 선택 화면을 띄우고 싶다면:
          // queryParams: { prompt: 'select_account' }
        },
      })

      if (authError) {
        console.error('Google login error:', authError.message)
        setError(authError.message ?? 'Google 로그인 중 오류가 발생했습니다.')
        setIsLoading(false)
        return
      }

      // 성공 시 Supabase가 알아서 구글 페이지로 이동시키므로 추가 리다이렉트 불필요
    } catch (err) {
      console.error('Google login error:', err)
      setError('Google 로그인 중 오류가 발생했습니다.')
      setIsLoading(false)
    }
  }

  return (
    <div className="min-h-screen bg-[#0D0D0F] text-white flex items-center justify-center px-4 py-10 relative overflow-hidden">
      <motion.div
        className="absolute inset-0 opacity-40"
        animate={{
          background: [
            'radial-gradient(circle at 20% 20%, rgba(0,255,194,0.25), transparent 55%)',
            'radial-gradient(circle at 80% 60%, rgba(0,200,255,0.2), transparent 55%)',
          ],
        }}
        transition={{ duration: 6, repeat: Infinity, repeatType: 'mirror' }}
      />

      <div className="relative z-10 w-full max-w-md bg-white/5 backdrop-blur-xl border border-white/10 rounded-3xl shadow-2xl p-8 space-y-8">
        {/* Header */}
        <div className="space-y-2 text-center">
          <div className="inline-flex items-center gap-2 text-[#00FFC2] text-sm font-semibold tracking-widest uppercase">
            <Sparkles className="w-4 h-4" />
            Derma AI
          </div>
          <h1 className="text-3xl font-bold">피부 고민, 이제 AI와 해결하세요</h1>
          <p className="text-gray-400 text-sm">Google 계정으로 빠르고 안전하게 시작해요.</p>
        </div>

        {/* Google Login Button */}
        <div className="space-y-4">
          {error && <p className="text-sm text-red-400">{error}</p>}

          <button
            onClick={handleGoogleLogin}
            disabled={isLoading}
            className="w-full flex items-center justify-center gap-3 bg-white text-black font-semibold py-4 px-6 rounded-2xl shadow-lg hover:bg-gray-50 transition-colors disabled:opacity-60 disabled:cursor-not-allowed"
          >
            {isLoading ? (
              <Loader2 className="w-5 h-5 animate-spin" />
            ) : (
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="#34A853"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="#FBBC05"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="#EA4335"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
            )}
            <span>Google 계정으로 시작하기</span>
          </button>
        </div>

        {/* Footer */}
        <div className="text-center">
          <p className="text-xs text-gray-500">
            로그인하면 이용약관 및 개인정보처리방침에 동의하게 됩니다.
          </p>
        </div>
      </div>
    </div>
  )
}



